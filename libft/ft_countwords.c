#include "libft.h"

size_t		ft_countwords(const char *s, char c)
{
	int	i;
	int	n;

	i = 0;
	n = 0;
	while (s[i] != '\0')
	{
		if ((s[i] == c || i == 0) && s[i + 1] != c && s[i + 1] != '\0')
			n++;
		i++;
	}
	return (n);
}
